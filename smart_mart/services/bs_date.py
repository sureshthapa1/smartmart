# Reference: 1970 BS Baisakh 1 = 1913 AD April 15
# Verified: 2081 BS Baisakh 1 = 2024 AD April 13 (Nepali New Year)
_REF_AD = date(1913, 4, 15)
_REF_BS_YEAR = 1970

# Build a lookup: BS year → start index in total-days-from-ref
_YEAR_START: dict[int, int] = {}
_total = 0
for _y, _months in _BS_MONTHS:
    _YEAR_START[_y] = _total
    _total += sum(_months)

_MONTH_NAMES_EN = [
    "Baisakh", "Jestha", "Ashadh", "Shrawan",
    "Bhadra", "Ashwin", "Kartik", "Mangsir",
    "Poush", "Magh", "Falgun", "Chaitra",
]
_MONTH_NAMES_NP = [
    "बैशाख", "जेठ", "असार", "श्रावण",
    "भाद्र", "आश्विन", "कार्तिक", "मंसिर",
    "पौष", "माघ", "फाल्गुन", "चैत्र",
]
_NEPALI_DIGITS = str.maketrans("0123456789", "०१२३४५६७८९")


def _to_nepali_digits(n: int) -> str:
    return str(n).translate(_NEPALI_DIGITS)


def to_bs(ad: date) -> tuple[int, int, int]:
    """Convert a Gregorian date to (BS year, BS month, BS day)."""
    delta = (ad - _REF_AD).days
    if delta < 0:
        raise ValueError(f"Date {ad} is before the supported range (1913-04-13)")

    for i, (bs_year, months) in enumerate(_BS_MONTHS):
        year_days = sum(months)
        if delta < year_days:
            for m_idx, m_days in enumerate(months):
                if delta < m_days:
                    return bs_year, m_idx + 1, delta + 1
                delta -= m_days
        delta -= year_days

    raise ValueError(f"Date {ad} is beyond the supported BS range")


def bs_format(ad: date | None, nepali: bool = True, show_month_name: bool = True) -> str:
    """Return a formatted BS date string.

    Args:
        ad: Gregorian date (or datetime — only date part used)
        nepali: If True, use Nepali script and digits. If False, use English.
        show_month_name: If True, show month name. If False, show month number.

    Returns:
        e.g. "२०८१ बैशाख १" (nepali=True) or "2081 Baisakh 1" (nepali=False)
    """
    if ad is None:
        return ""
    try:
        # Accept datetime objects too
        if hasattr(ad, "date"):
            ad = ad.date()
        y, m, d = to_bs(ad)
        if nepali:
            month = _MONTH_NAMES_NP[m - 1] if show_month_name else _to_nepali_digits(m)
            return f"{_to_nepali_digits(y)} {month} {_to_nepali_digits(d)}"
        else:
            month = _MONTH_NAMES_EN[m - 1] if show_month_name else str(m)
            return f"{y} {month} {d}"
    except Exception:
        return ""
